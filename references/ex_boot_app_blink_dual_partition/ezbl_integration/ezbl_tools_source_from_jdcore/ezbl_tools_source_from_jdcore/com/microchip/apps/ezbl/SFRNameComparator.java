import java.util.Comparator;
import java.util.regex.Matcher;
import java.util.regex.Pattern;



































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































class SFRNameComparator
  implements Comparator<String>
{
  SFRNameComparator() {}
  
  public int compare(String x, String y)
  {
    if ((x == null) && (y != null))
    {
      return -1;
    }
    if ((y == null) && (x != null))
    {
      return 1;
    }
    if (x == null)
    {
      return 0;
    }
    if (y == null)
    {
      return 0;
    }
    if (x.equals(y))
    {
      return 0;
    }
    if ((x.isEmpty()) && (!y.isEmpty()))
    {
      return -1;
    }
    if ((y.isEmpty()) && (!x.isEmpty()))
    {
      return 1;
    }
    


    Pattern p = Pattern.compile("([^0-9]+)([0-9]*)", 32);
    

    Matcher mx = p.matcher(x);
    Matcher my = p.matcher(y);
    while ((mx.find()) && (my.find()))
    {
      String tokenx = mx.group(1);
      String tokeny = my.group(1);
      if (!tokenx.equals(tokeny))
      {
        return tokenx.compareTo(tokeny);
      }
      int numx = Integer.parseInt(mx.group(2));
      int numy = Integer.parseInt(my.group(2));
      if (numx < numy)
      {
        return -1;
      }
      if (numx > numy)
      {
        return 1;
      }
    }
    return x.compareTo(y);
  }
}
